/*
POO - Programacao Orientada a Objetos
A base sao as classes e os objetos. Todo objeto é criado a partir de uma classe
A classe é como se fosse um molde onde sao definidos os atributos e os metodos que um objeto vai receber
*/

class Carro {
}
// OBS: Em uma classe a primeira letra deve ser maiuscula

let landRover = new Carro()
// Abrimos e fechamos parenteses assim como quando criamos uma funcao

console.log(landRover)
// landRover é um objeto da classe Carro

